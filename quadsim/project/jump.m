function [out,use_torque,kp,kd] = jump(t)

    % small jump
    if (t < 0.5)
        out = [-.1,  1.3,   -2, ...
                .1,  1.3,   -2, ...
               -.1,   .8, -1.5, ...
                .1,   .8, -1.5];
    else
        out = [-.1, 1.3, 100, ...
                .1, 1.3, 100, ...
               -.1,  .8, 100, ...
                .1,  .8, 100];
    end

    % big jump
    if (t < 0.5)
        out = [-.1,  1.3, -2, ...
                .1,  1.3, -2, ...
               -.1,  1.3, -2, ...
                .1,  1.3, -2];
    else
        out = [-.1, 1.3, 100, ...
                .1, 1.3, 100, ...
               -.1, 1.3, 100, ...
                .1, 1.3, 100];
    end

    use_torque = false;
    kp = 100*ones(1,12);
    kd = 1*ones(1,12);
end
